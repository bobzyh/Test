package com.st.stmall.pojo;

public class Goods {
	private Integer gid;
	private String gname;
	private String gdept;
	private Double gprice;
	private int gcount;
	private int gsales;
	private int gftype;
	private int gstype;
	private String gpic;
	private int gstatus;
	
	public Integer getGid() {
		return gid;
	}
	public void setGid(Integer gid) {
		this.gid = gid;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGdept() {
		return gdept;
	}
	public void setGdept(String gdept) {
		this.gdept = gdept;
	}
	public Double getGprice() {
		return gprice;
	}
	public void setGprice(Double gprice) {
		this.gprice = gprice;
	}
	public int getGcount() {
		return gcount;
	}
	public void setGcount(int gcount) {
		this.gcount = gcount;
	}
	public int getGsales() {
		return gsales;
	}
	public void setGsales(int gsales) {
		this.gsales = gsales;
	}
	public int getGftype() {
		return gftype;
	}
	public void setGftype(int gftype) {
		this.gftype = gftype;
	}
	public int getGstype() {
		return gstype;
	}
	public void setGstype(int gstype) {
		this.gstype = gstype;
	}
	public String getGpic() {
		return gpic;
	}
	public void setGpic(String gpic) {
		this.gpic = gpic;
	}
	public int getGstatus() {
		return gstatus;
	}
	public void setGstatus(int gstatus) {
		this.gstatus = gstatus;
	}
}
